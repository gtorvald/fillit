#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;
	int		i;
	int		size;

	tmp = (char *)s;
	size = (int)n;
	i = 0;
	while (i < size)
		tmp[i++] = 0;
}
